        <h3>Bolo de cenoura</h3>
        <p>O bolo de cenoura é uma deliciosa receita clássica que combina a doçura da cenoura com a cremosidade do leite.</p>
        <p>Ingredientes:</p>
        <ul>
            <li>1 xícara (chá) de cenoura picada</li>
            <li>2 xícaras (chá) de leite</li>
            <li>1 xícara (chá) de açúcar</li>
            <li>1 xícara (chá) de óleo de coco</li>
            <li>2 xícaras (chá) de farinha de trigo</li>
            <li>1 colher (sobremesa) de fermento em pó</li> 
        </ul>
        <p>Modo de Preparo:</p> 
        <ol>
            <li>Em uma tigela, misture a cenoura, o leite, o açúcar, o óleo de coco e a farinha de trigo.</li>
            <li>Despeje a mistura em uma forma untada e leve ao forno preaquecido a 180°C por 40 minutos.</li>
            <li>Desligue o forno e deixe descansar por 10 minutos antes de servir.</li>
        </ol>
